# Use a variable to repesent a person's name, and then print that person's name in lowercase, uppercase, and title case

first_name = 'keitron'
last_name = 'wallace'

lower_case = f"This name will be in lower case: {first_name.lower()} {last_name.lower()}."
upper_case = f"This name will be in upper case: {first_name.upper()} {last_name.upper()}."
title_case = f"This name will be capitalized: {first_name.title()} {last_name.title()}."

print(lower_case)
print(upper_case)
print(title_case)